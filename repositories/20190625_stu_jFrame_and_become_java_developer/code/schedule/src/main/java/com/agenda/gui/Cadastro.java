    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        cadastro = new UserDao();
        if (cadastro.verificarLogin(jTextField3.getText())) {
            JOptionPane.showMessageDialog(null, "Usuário já Cadastrado!");
            this.dispose();
        } else {
            newUser = new User();
            newUser.cadastroUser(
                    jTextField1.getText(), //nome
                    jTextField2.getText(), //email
                    jTextField3.getText(), //login
                    jPasswordField1.getText());  //pass
            if (cadastro.cadastrarUser(newUser)) {
                JOptionPane.showMessageDialog(null, "Usuário cadastrado com sucesso! \n \n Bem Vindo, " + newUser.getNome() + "   :-) .");
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Alguma falha ao cadastrar, \n entre em contato com o administrador");
                this.dispose();
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

