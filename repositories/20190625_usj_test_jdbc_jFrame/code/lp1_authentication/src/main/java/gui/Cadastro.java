    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jPasswordField1.getText().length() >= 6) { // .getSize()
            cadastro = new UserDao();
            if (!cadastro.VerificarEmail(jTextField2.getText())) {
                User novoUser = new User();
                novoUser.CadastrarUser(jTextField1.getText(), jTextField2.getText(), jPasswordField1.getText());
                cadastro.CadastrarUser(novoUser);
                JOptionPane.showMessageDialog(null, "Email Cadastrado com sucesso!");
                setVisible(false);
            } else {
                JOptionPane.showMessageDialog(null, "Email já cadastrado!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Senha invalida, escreva uma com pelo menos 6 caracteres!");
            jPasswordField1.setText("  S E N H A");
            jPasswordField1.setEchoChar((char) 0);
            passKey = false;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

